		<h1 align="center"><span style="color: darkblue;"> Who can give <span style="color: red;"> Blood, <span style="color: darkblue;"> and often? </h1>


			<h2 align="center"> <span style="color: black;"> The criteria for donor selection varies from country to country, but blood can be donated by most people who are healthy and do not have an infection that can be transmitted through their blood.

The age at which people are eligible to give blood varies, but is commonly between the ages of 17 and 65. Some countries accept donations from people from the age of 16 and extend the upper age limit beyond 65 years.

Healthy adults can give blood regularly – at least twice a year. Your local blood service can tell you how frequently you can give blood. </h2> <br><br><br>

<h1 align="center"><span style="color: darkblue;"> Who should not give <span style="color: red;"> Blood?  </h1>

<h2 align="center"> <span style="color: black;"> The <span style="color: red;"> Blood <span style="color: black;"> service is concerned with the welfare of both the blood donor and the recipient (patient).  Donors are often asked not to donate blood for a period of time in the interests of their safety and/or that of the blood supply.<br>

You should not give blood if your own health might suffer as a result. The first concern of the blood service is to ensure that blood donation does no harm to the blood donor. You should not donate blood if:<br>

You are feeling unwell;
You are anaemic;
You are pregnant, have been pregnant within the last year or are breastfeeding;
You have certain medical conditions, which might make you an unsuitable donor ;
You are taking certain medications, such as antibiotics.
You may be able to donate blood at a later time. In some cases, however, in order to protect your own health you will not be able to donate blood.<br>

You should not donate blood if it might cause harm to the patient who receives it. Blood can transmit life-threatening infections to patients who receive blood transfusions. You should not donate blood if:<br>

You have or may recently have contracted a sexually transmitted disease, such as HIV or syphilis, that can be passed on to a patient who receives your blood.<br>
Your lifestyle puts you at risk of contracting an infection that can be transmitted through your blood: for example, if you have more than one sexual partner or have sexual contact with prostitutes
You have ever injected recreational, non-medicinal drugs
You have recently had a tattoo, skin scarification or ear or body piercing – your local blood service can tell you how long you must wait before giving blood.
You have had sexual contact with anyone in the above categories. </h2> <br><br><br>



